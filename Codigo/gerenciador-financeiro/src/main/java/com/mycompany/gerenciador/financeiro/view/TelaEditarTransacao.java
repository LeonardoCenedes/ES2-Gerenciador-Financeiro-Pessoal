    private void preencherCampos() {
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
        txtData.setText(sdf.format(transacao.getData()));
        txtValor.setText(String.valueOf(transacao.getValor()));

        // ✅ NOVO: Seleciona a categoria no ComboBox
        for (int i = 0; i < categoriasDisponiveis.size(); i++) {
            if (categoriasDisponiveis.get(i).getNome().equals(transacao.getCategoria().getNome())) {
                cbCategoria.setSelectedIndex(i);
                break;
            }
        }

        txtDescricao.setText(transacao.getDescricao());
        txtComprovante.setText(transacao.getComprovante());

        if (transacao.getTipo() == TiposTransacao.ENTRADA) {
            cbTipo.setSelectedIndex(0);
        } else {
            cbTipo.setSelectedIndex(1);
        }

        txtConta.setText(transacao.getConta().getNome());
        txtConta.setEditable(false);
    }

    private void carregarCategorias() {
        try {
            categoriasDisponiveis = categoriaController.buscarCategoriasAtivas();

            DefaultComboBoxModel<String> model = new DefaultComboBoxModel<>();
            for (Categoria categoria : categoriasDisponiveis) {
                model.addElement(categoria.getNome());
            }
            cbCategoria.setModel(model);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao carregar categorias: " + e.getMessage());
        }
    }

