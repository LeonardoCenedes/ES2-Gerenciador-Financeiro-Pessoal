    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {
        try {
            SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
            Date data = sdf.parse(txtData.getText());

            float valor = Float.parseFloat(txtValor.getText());

            // ✅ NOVO: Pega categoria do ComboBox
            int indiceCategoria = cbCategoria.getSelectedIndex();
            if (indiceCategoria < 0) {
                JOptionPane.showMessageDialog(this, "Selecione uma categoria!");
                return;
            }
            Categoria categoria = categoriasDisponiveis.get(indiceCategoria);

            String descricao = txtDescricao.getText();
            String comprovante = txtComprovante.getText();

            String tipoSelecionado = (String) cbTipo.getSelectedItem();
            TiposTransacao tipo = TiposTransacao.valueOf(tipoSelecionado);

            boolean sucesso = controller.editarTransacao(transacao, data, valor,
                    categoria, descricao,
                    comprovante, tipo);

            if (sucesso) {
                JOptionPane.showMessageDialog(this, "Transação atualizada com sucesso!");
                dispose();
            }

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Valor inválido! Digite um número válido.");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro: " + e.getMessage());
        }
    }

