    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        try {
        // Ler os dados dos campos
        String nome = txtNome.getText();
        String tipo = (String) cbTipo.getSelectedItem();
        double saldoInicial = Double.parseDouble(txtSaldo.getText());
        String moeda = (String) cbMoeda.getSelectedItem();

        // Chamar o controller para cadastrar
        controller.cadastrarConta(nome, tipo, saldoInicial, moeda);

        // Mostrar mensagem de sucesso
        JOptionPane.showMessageDialog(this, "Conta cadastrada com sucesso!");

        // Limpar os campos
        limparCampos();

    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "O saldo inicial deve ser um número válido.");
    } catch (IllegalArgumentException e) {
        JOptionPane.showMessageDialog(this, e.getMessage());
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Erro ao salvar conta: " + e.getMessage());
    }
    }//GEN-LAST:event_btnSalvarActionPerformed

