    /**
     * Limpa todos os campos do formulário
     */
    private void limparCampos() {
        txtNome.setText("");
        cbTipo.setSelectedIndex(0);
        txtSaldo.setText("");
        cbMoeda.setSelectedIndex(0);
        txtNome.requestFocus();
    }
    
